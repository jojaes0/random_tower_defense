/** 무한모드(51R~) 외삽 — 보스는 50, 일반은 49 기준으로 라운드당 +15% */
export const endlessHellHp = (round: number): number => {
  const isBoss = round % 10 === 0
  const anchor = isBoss ? ROUND_HP_HELL[49] : ROUND_HP_HELL[48]
  return anchor * Math.pow(1.15, round - 50)
}
